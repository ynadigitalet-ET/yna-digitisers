export function SocialLinks({ links, className = "", iconClassName = "w-5 h-5" }: { links: SocialLink[]; className?: string; iconClassName?: string }) {
  if (links.length === 0) {
    return null;
  }
  return (
    <div className={`flex flex-wrap items-center gap-3 ${className}`}>
      {links.map((link) => (
        <a
          key={`${link.platform}-${link.url}`}
          href={link.url}
          target="_blank"
          rel="noreferrer"
          aria-label={link.platform}
          title={link.platform}
          className="flex h-10 w-10 items-center justify-center rounded-full border border-border bg-background text-foreground transition hover:border-brand-blue hover:text-brand-blue hover:-translate-y-0.5"
        >
          <SocialGlyph platform={link.platform} className={iconClassName} />
        </a>
      ))}
    </div>
  );
}
